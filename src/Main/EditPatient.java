    private void loadtotextfield(){
        String id = Integer.toString(editid);
        try {
        con= Connector.ConnectDb();
        
        String sql = "select * from patient where patientid=?";
        ps=con.prepareStatement(sql);
        ps.setString(1,id);
        rs=ps.executeQuery();
        }
        catch (SQLException E){
            JOptionPane.showMessageDialog(null,"before result");
            JOptionPane.showMessageDialog(null,E);
        }
        try{
            rs.next();
                firstNameText.setText(rs.getString("firstName"));
                lastNameText.setText(rs.getString("lastName"));
                PhoneText.setText(rs.getString("mobileno"));
                genderBox.setSelectedItem(rs.getString("gender"));
                AgeText.setText(Integer.toString(rs.getInt("age")));
                bloodBox.setSelectedItem(rs.getString("bloodgroup"));
                AddressText.setText(rs.getString("address"));
                diseaseText.setText(rs.getString("disease"));
                PatientType.setSelectedItem(rs.getString("patient_type"));
               
        }
               catch(SQLException E){
                   JOptionPane.showMessageDialog(null,E);
               }
    }
    
