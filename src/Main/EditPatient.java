    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        con = Connector.ConnectDb();
        if (con != null) {
            String fname = firstNameText.getText();
            String lname = lastNameText.getText();
            int age = Integer.parseInt(AgeText.getText());
            String gender = (String) genderBox.getSelectedItem();
            String blood = (String)bloodBox.getSelectedItem();
            String phone = PhoneText.getText();
            String disease = diseaseText.getText();
            String address = AddressText.getText();
            String patienttype = (String)(PatientType.getSelectedItem());
            String usql="update Patient set firstName = ?,lastName = ?, mobileNo = ?, gender = ?, age = ? , bloodgroup = ?, Address = ?, disease = ?, patient_type = ? WHERE patientId="+Integer.toString(editid);
            try {
                ps = con.prepareStatement(usql);
                ps.setString(1, fname);
                ps.setString(2, lname);
                ps.setString(3, phone);
                ps.setString(4, gender);
                ps.setInt(5, age);
                ps.setString(6, blood);
                ps.setString(7, address);
                ps.setString(8, disease);
                ps.setString(9, patienttype);
                ps.execute();
                JOptionPane.showMessageDialog(null, "Data Saved");
                dispose();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_SaveActionPerformed

